// Javascript - საშინაო დავალება
// ლექცია 6

// დავალება 1: მომხმარებლის მონაცემების ვალიდაცია (String + Number + if + while)
// ამოცანა:
// მომხმარებელს უნდა შეაყვანინო სახელი და ასაკი.
// 1.	პირველ ეტაპზე შეეკითხე მომხმარებელს სახელი
// o	თუ სახელის სიგრძე (length) ნაკლებია 3-ზე, ისევ ჰკითხე (while ლუპით)
// 2.	შემდეგ შეეკითხე ასაკი
// o	თუ შეყვანილი მნიშვნელობა არ არის რიცხვი (isNaN)
// o	ან ასაკი არის ნაკლები 18-ზე
// → ისევ ჰკითხე ასაკი
// 3.	როცა ორივე მონაცემი სწორად იქნება შეყვანილი:
// o	გვერდზე გამოიტანე:
// 	მომხმარებლის სახელი
// 	ასაკი
// 	შეტყობინება: „მონაცემები მიღებულია წარმატებით”
// 📌 გამოსაყენებელი თემები:
// •	prompt
// •	string.length
// •	Number()
// •	isNaN()
// •	if
// •	while
// •	document.write
// შესრულება:
let saxeli = prompt("შეიყვანეთ თქვენი სახელი");
while (saxeli === null || saxeli.trim().length < 3) {
  saxeli = prompt("შეიყვანეთ თქვენი სახელი");
}
let asaki = Number(prompt("შეიყვანეთ თქვენი ასაკი"));
while (isNaN(asaki) || asaki < 18) {
  asaki = Number(prompt("შეიყვანეთ თქვენი ასაკი"));
}
document.write(`<h2> მომხმარებლის სახელი: ${saxeli} </h2><br>`);
document.write(`<h2> მომხმარებლის ასაკი: ${asaki}</h2> <br>`);
document.write(`<h2> „მონაცემები მიღებულია წარმატებით”</h2> <br>`);

// დავალება 2: ელფოსტის მინიმალური ვალიდაცია (String + if + while)
// ამოცანა:
// მომხმარებელს შეეკითხე ელფოსტა.
// •	სანამ შეყვანილი ტექსტი არ შეიცავს @ სიმბოლოს
// → ისევ ჰკითხე ელფოსტა (while)
// •	სწორი შეყვანის შემდეგ გვერდზე გამოიტანე:
// o	„ელფოსტა მიღებულია“
// 📌 გამოიყენე: string.includes(), if, while, prompt, document.write
let elposta = prompt("შეიყვანეთ თქვენი ელფოსტა");
while (!elposta.includes("@")) {
  elposta = prompt("შეიყვანეთ თქვენი ელფოსტა");
}
document.write(`<h2> „ელფოსტა მიღებულია”</h2> <br>`);
// დავალება 3: ფასის სწორად შეყვანა (Number + if + while)
// ამოცანა:
// მომხმარებელს შეეკითხე პროდუქტის ფასი.
// •	თუ შეყვანილი მნიშვნელობა:
// o	არ არის რიცხვი (isNaN)
// o	ან ნაკლებია ან ტოლია 0-ის
// → ისევ ჰკითხე ფასი
// •	სწორად შეყვანის შემდეგ:
// o	გვერდზე გამოიტანე ფასი
// 📌 გამოიყენე: Number(), isNaN(), if, while
let price = Number(prompt("შეიყვანეთ პროდუქტის ფასი"));
while (isNaN(price) || price <= 0) {
  price = Number(prompt("შეიყვანეთ პროდუქტის ფასი"));
}
document.write(`<h2> პროდუქტის ფასია: ${price}</h2> <br>`);
// დავალება 4: შემთხვევითი ქულა და შეფასება (Math + if)
// ამოცანა:
// დააგენერირე შემთხვევითი ქულა 0-დან 100-ს ჩათვლით.
// •	თუ ქულა ≥ 90 → „შესანიშნავია“
// •	თუ ქულა ≥ 60 → „კარგია“
// •	სხვა შემთხვევაში → „საჭიროა გაუმჯობესება“
// შედეგი გამოიტანე გვერდზე.
// 📌 გამოიყენე: Math.random(), Math.floor(), if
let rndmqula = Math.floor(Math.random() * 100);
if (rndmqula >= 90) {
  document.write(`შესანიშნავია! ${rndmqula} <br>`);
} else if (rndmqula >= 60) {
  document.write(`კარგია! ${rndmqula} <br>`);
} else {
  document.write(`საჭიროა გაუმჯობესება! ${rndmqula} <br>`);
}
// დავალება 5: ტელეფონის ნომრის შემოწმება
// ამოცანა:
// მომხმარებელს შეეკითხე ტელეფონის ნომერი.
// თუ სიმბოლოების რაოდენობა 9 არ არის, აჩვენე შეცდომა.
// სხვა შემთხვევაში გამოიტანე „ნომერი მიღებულია“.
// 📌 გამოიყენე: string.length, if
let tel = prompt("შეიყვანეთ ტელეფონის ნომერი");
if (tel === null || tel.trim().length < 9) {
  document.write(`<p>"არასწორი ტელეფონის ნომერი"</p>`);
} else {
  document.write(`<p>"ნომერი მიღებულია"</p>`);
}
// დავალება 6: პაროლის სწორად შეყვანა (while)
// ამოცანა:
// მომხმარებელს შეეკითხე პაროლი.
// თუ პაროლი არ არის "admin123", ისევ ჰკითხე.
// ციკლი გაგრძელდეს მანამ, სანამ სწორად არ შეიყვანს.
// სწორი შეყვანის შემდეგ გამოიტანე შეტყობინება.
// 📌 გამოიყენე: while, prompt, if
let passWord = prompt("შეიყვანეთ პაროლი");
while (passWord === null || !(passWord === "admin123")) {
  passWord = prompt("შეიყვანეთ პაროლი");
}
document.write(`<p>${passWord}</p>`);
// დავალება 7: სახელის ასოების დათვლა (for)
// ამოცანა:
// მომხმარებელს შეეკითხე სახელი.
// for ლუპის გამოყენებით დაითვალე რამდენი ასოა სახელში
// და გამოიტანე შედეგი.
// 📌 გამოიყენე: string.length, for
let saxeli2 = prompt("შეიყვანეთ თქვენი სახელი");
let sigrdze = 0;
for (i = 0; i < saxeli2.length; i++) {
  sigrdze = i;
}
document.write(`<p>${sigrdze + 1}</p>`);
// დავალება 8: რიცხვების ჯამი (for)
// ამოცანა:
// მომხმარებელს შეეკითხე ერთი რიცხვი.
// for ლუპის გამოყენებით დაითვალე 1-დან ამ რიცხვამდე ყველა რიცხვის ჯამი
// და გამოიტანე გვერდზე.
// 📌 გამოიყენე: for, არითმეტიკა
let ricxvi = Number(prompt("შეიყვანეთ რაიმე ნატურალური რიცხვი"));
while (isNaN(ricxvi) || ricxvi <= 0) {
  ricxvi = Number(prompt("შეიყვანეთ რაიმე ნატურალური რიცხვი"));
}
let summ = 0;
for (i = 0; i <= ricxvi; i++) {
  summ += i;
}
document.write(
  `<p>1-დან ${ricxvi}-მდე (ჩათვლით) ნატურალური რიცხვების ჯამია: ${summ}</p>`,
);
